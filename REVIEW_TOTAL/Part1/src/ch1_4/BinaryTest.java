package ch1_4;

public class BinaryTest {
    public static void main(String[] args) {
        int num = 10;  // Decimal
        int bNum = 0b1010;  // Binary
        int oNum = 012;  // Octal
        int xNum = 0xA;  // Hexadecimal

        System.out.println(num);
        System.out.println(bNum);
        System.out.println(oNum);
        System.out.println(xNum);
    }
}
